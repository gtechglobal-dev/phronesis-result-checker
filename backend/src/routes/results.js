const express = require('express')
const router = express.Router()
const resultController = require('../controllers/resultController')
const protect = require('../middlewares/auth')
const authorize = require('../middlewares/roleAuth')

router.post('/check', resultController.checkByRegNo)
router.get('/pending', protect, authorize('EXAM_OFFICER'), resultController.getPendingResults)
router.get('/parent-children', protect, authorize('PARENT'), resultController.getParentChildrenResults)
router.get('/my-class', protect, authorize('FORM_TEACHER', 'SUBJECT_TEACHER', 'EXAM_OFFICER'), resultController.getFormTeacherClassResults)
router.get('/:id', protect, resultController.getResult)
router.get('/student/:studentId', protect, resultController.getStudentResults)
router.post('/', protect, authorize('EXAM_OFFICER', 'FORM_TEACHER', 'SUBJECT_TEACHER'), resultController.createResult)
router.put('/:id/comment', protect, authorize('FORM_TEACHER'), resultController.addTeacherComment)
router.put('/:id/principal-comment', protect, authorize('EXAM_OFFICER'), resultController.updatePrincipalComment)
router.put('/:id/withhold', protect, authorize('EXAM_OFFICER'), resultController.toggleWithhold)
router.put('/:id/status', protect, authorize('EXAM_OFFICER'), resultController.updateResultStatus)
router.get('/manage/all', protect, authorize('EXAM_OFFICER'), resultController.getManageResults)
router.post('/positions', protect, authorize('EXAM_OFFICER'), resultController.updatePositions)

module.exports = router
